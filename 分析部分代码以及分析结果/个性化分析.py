# ==================== 2. 中文分词函数 ====================
def chinese_tokenizer(text):
    """使用jieba进行中文分词，返回词列表"""
    words = jieba.lcut(text)
    # 过滤掉单字词、标点符号和停用词（可根据需要添加停用词表）
    stopwords = set(['的', '了', '是', '在', '和', '与', '或', '及', '等', '，', '。', '！', '？', '；', '：', '“', '”', '（', '）'])
    return [w for w in words if len(w) > 1 and w not in stopwords]

# 对所有评语进行分词
