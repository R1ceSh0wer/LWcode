# 将上三角索引转换为原始矩阵的(i,j)索引
def pair_index_to_ij(idx, n):
    # idx是上三角展开的索引，对应行i，列j，其中i<j
    # 计算对应的i,j
    i = n - 2 - int(np.sqrt(-8*idx + 4*n*(n-1)-7)/2.0 - 0.5)
    j = idx + i + 1 - n*(n-1)//2 + (n-i)*((n-i)-1)//2
    return int(i), int(j)

i_max, j_max = pair_index_to_ij(max_cos_idx, n)
i_min, j_min = pair_index_to_ij(min_cos_idx, n)

