word_sets = [set(tokens) for tokens in tokenized_comments]  # 用于Jaccard

print(f"分词完成，平均每份评语词数：{np.mean([len(t) for t in tokenized_comments]):.1f}")

# ==================== 3. 计算 Jaccard 相似系数 ====================
def jaccard_similarity(set1, set2):
    """计算两个集合的Jaccard相似系数"""
    if len(set1) == 0 or len(set2) == 0:
        return 0
    intersection = len(set1 & set2)
    union = len(set1 | set2)
    return intersection / union

n = len(comments)
jaccard_matrix = np.zeros((n, n))

for i in range(n):
    for j in range(i+1, n):
        sim = jaccard_similarity(word_sets[i], word_sets[j])
        jaccard_matrix[i, j] = sim
        jaccard_matrix[j, i] = sim

# 提取上三角部分的非对角线元素（即两两之间不重复的相似度）
jaccard_pairs = jaccard_matrix[np.triu_indices_from(jaccard_matrix, k=1)]
print("\n========== Jaccard 相似系数统计 ==========")
print(f"有效比较对数: {len(jaccard_pairs)}")
print(f"平均Jaccard相似度: {np.mean(jaccard_pairs):.4f}")
print(f"标准差: {np.std(jaccard_pairs):.4f}")
print(f"最小值: {np.min(jaccard_pairs):.4f}")
